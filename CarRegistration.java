    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Modify the customer data to tblCarInfo
        int i = tblCarInfo.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel)tblCarInfo.getModel();
        if (i >=0)
        {
            model.setValueAt(txtCarID.getText(), i, 0);
            model.setValueAt(txtBrand.getText(), i, 1);
            model.setValueAt(txtModel.getText(), i, 2);
            model.setValueAt(ComboSeats.getSelectedItem().toString(), i, 3);
            model.setValueAt(ComboTrans.getSelectedItem().toString(), i, 4);
            model.setValueAt(txtPrice.getText(), i, 5);
            model.setValueAt(ComboAvl.getSelectedItem().toString(), i, 6);
            JOptionPane.showMessageDialog(null,"Successful Edit");
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Error");
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // Delete data from the tblCarInfo
        DefaultTableModel model = (DefaultTableModel)tblCarInfo.getModel();
        int SelRow = tblCarInfo.getSelectedRow();
        int dialog = JOptionPane.showConfirmDialog(this,"Do you want to delete the record","Warning",JOptionPane.YES_NO_OPTION); 
        if (SelRow >= 0 && dialog == JOptionPane.YES_OPTION){
            model.removeRow(SelRow);
            JOptionPane.showMessageDialog(null, "Successfully Delete");
        }
        else{
            JOptionPane.showMessageDialog(null, "No Row Selected OR You not want Delete");
        }
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

