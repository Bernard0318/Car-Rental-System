    private void tblCarInfoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCarInfoMouseClicked
        int selectedRow = tblCarInfo.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel)tblCarInfo.getModel();
        txtCarID.setText(model.getValueAt(selectedRow, 0).toString());
        txtBrand.setText(model.getValueAt(selectedRow, 1).toString());
        txtModel.setText(model.getValueAt(selectedRow, 2).toString());
        ComboSeats.setSelectedItem(model.getValueAt(selectedRow, 3).toString());
        ComboTrans.setSelectedItem(model.getValueAt(selectedRow, 4).toString());
        txtPrice.setText(model.getValueAt(selectedRow, 5).toString());
        ComboAvl.setSelectedItem(model.getValueAt(selectedRow, 6).toString());
        
    }//GEN-LAST:event_tblCarInfoMouseClicked

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        DefaultTableModel model = (DefaultTableModel)tblCarInfo.getModel();
        model.setRowCount(0);
        File file = new File("CarInformation.txt");
        try
        {
            BufferedReader br = new BufferedReader(new FileReader(file));
            Object[] CarInfoLines = br.lines().toArray();
                for (int i=0; i<CarInfoLines.length;i++)
                {
                    String Line = CarInfoLines[i].toString().trim();
                    String[] dataRow = Line.split(" ");
                    model.addRow(dataRow);
                }
        }
                
        catch(IOException Ex)
        {
            
        }
        
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        try{
            File file = new File("CarInformation.txt");
            if (!file.exists()){
                file.createNewFile();
            }
            
            FileWriter fw = new FileWriter(file.getAbsoluteFile());
            BufferedWriter bw = new BufferedWriter(fw);
            
            for (int i=0; i< tblCarInfo.getRowCount(); i++){
                for (int j=0; j< tblCarInfo.getColumnCount(); j++){
                    bw.write ((String)tblCarInfo.getModel().getValueAt(i, j)+" ");
                }
                bw.write("\n");
            }
            bw.close();
            fw.close();
            JOptionPane.showMessageDialog(null,"Data Saved");
                
        }
        catch(Exception ex){
            
        }
        
    }//GEN-LAST:event_jButton6ActionPerformed

